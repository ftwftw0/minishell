#include "libft.h"

void	ft_freetab(char ***tab)
{
	int i;

	if (!tab || !(*tab))
		return ;
	i = -1;
	if ((*tab))
	{
		while ((*tab)[++i])
		{
			free((*tab)[i]);
			(*tab)[i] = NULL;
		}
		free((*tab));
		(*tab) = NULL;
	}
}
