#include "libft.h"

int		ft_hidenp(char *s1, char *s2)
{
	static int		i = 0;
	static int		j = -1;

	while (s2[++j])
	{
		if (s1[i] == s2[j])
			i++;
	}
	if (!s1[i])
		return (1);
	else
		return (0);
}
