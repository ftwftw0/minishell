#include "libft.h"

int		ft_isspace(int c)
{
	c = (unsigned char)c;
	if (c == ' ')
		return (1);
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r')
		return (1);
	return (0);
}
